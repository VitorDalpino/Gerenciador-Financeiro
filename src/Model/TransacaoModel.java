package Model;

import java.time.LocalDate;

public class TransacaoModel {
    public int id;
    public double valor;
    public LocalDate data;
    public String descricao;
    public String tipo; // "receita" ou "despesa"
    public CategoriaModel categoria; // Associação com Categoria

    public TransacaoModel() {
    }

    public TransacaoModel(int id, double valor, LocalDate data, String descricao, String tipo, CategoriaModel categoria) {
        this.id = id;
        this.valor = valor;
        this.data = data;
        this.descricao = descricao;
        this.tipo = tipo;
        this.categoria = categoria;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public CategoriaModel getCategoria() {
        return categoria;
    }

    public void setCategoria(CategoriaModel categoria) {
        this.categoria = categoria;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public LocalDate getData() {
        return data;
    }

    public void setData(LocalDate data) {
        this.data = data;
    }

    public double getValor() {
        return valor;
    }

    public void setValor(double valor) {
        this.valor = valor;
    }
}
